package com.example.AlphaHealthAssistant.ui.caloriecal.database;

public class Lunch {
    private Integer redRiceCount;
    private Integer greenBeansCount;
    private Integer tomatoCount;
    private Integer mushroomCount;
    private String date;



    public Lunch() {

    }

    public Integer getRedRiceCount() {
        return redRiceCount;
    }

    public void setRedRiceCount(Integer redRiceCount) {
        this.redRiceCount = redRiceCount;
    }

    public Integer getGreenBeansCount() {
        return greenBeansCount;
    }

    public void setGreenBeansCount(Integer greenBeansCount) {
        this.greenBeansCount = greenBeansCount;
    }

    public Integer getTomatoCount() {
        return tomatoCount;
    }

    public void setTomatoCount(Integer tomatoCount) {
        this.tomatoCount = tomatoCount;
    }

    public Integer getMushroomCount() {
        return mushroomCount;
    }

    public void setMushroomCount(Integer mushroomCount) {
        this.mushroomCount = mushroomCount;
    }
    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
